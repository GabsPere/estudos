# Módulo que simula um servidor local

'''
os comandos a seguir são passados no CLI.
Para saber o endereço de ip no linux usamos "hostname -I" | Windowns "ip config".
Para iniciar o módulo -> "python3 -m http.server".
Para acessar podemos apertar no link que ficará no terminal ou usar o endereço de ip + :8000 (porta fornecida pelo módulo).
Para funcionar os dispositivos precisam estar conectados na mesma rede.
PS: O servidor será iniciado na pasta que utilizarmos o comando.
'''